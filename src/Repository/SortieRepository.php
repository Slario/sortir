    public function getOrganisateurId(Sortie $sortie)
    {
        $em = $this->getEntityManager();
        $rsm = new ResultSetMapping();
        $query = $em->createNativeQuery('SELECT organisateur_id FROM sortie WHERE sortie.id = ?', $rsm);
